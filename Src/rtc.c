void rtc_time_set(void)    
{

  RTC_TimeTypeDef sTime;
  RTC_DateTypeDef sDate;

  sTime.Hours = 10;
  sTime.Minutes = 55;
  sTime.Seconds = 00;
  sDate.Year = 19;
  sDate.Month = 11;
  sDate.Date = 2;

  HAL_RTC_SetTime(&hrtc, &sTime, FORMAT_BIN);
  HAL_RTC_SetDate(&hrtc, &sDate, FORMAT_BIN); 
}

